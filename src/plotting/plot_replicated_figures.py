def write_summary_tables(main: Dict[str, Dict[str, object]], run_group: str) -> None:
    rows = []
    for metric, _, _ in METRICS:
        for scenario in _ordered_scenarios(main.keys()):
            payload = main[scenario]
            _, arr = _stack_metric(payload, metric)
            if np.all(~np.isfinite(arr)):
                continue
            last = _summ(_last_values(arr))
            delta = _summ(_delta_values(arr))
            rows.append({
                "metric": metric,
                "scenario": scenario,
                "scenario_label": SCENARIO_LABELS.get(scenario, scenario),
                "n_runs": last["n"],
                "last_window": LAST_WINDOW,
                "last_mean": last["mean"],
                "last_sd": last["sd"],
                "last_ci95_low": last["ci_low"],
                "last_ci95_high": last["ci_high"],
                "last_median": last["median"],
                "last_min": last["min"],
                "last_max": last["max"],
                "delta_convention": "final_minus_initial",
                "delta_mean": delta["mean"],
                "delta_sd": delta["sd"],
                "delta_ci95_low": delta["ci_low"],
                "delta_ci95_high": delta["ci_high"],
                "delta_median": delta["median"],
                "delta_min": delta["min"],
                "delta_max": delta["max"],
            })
    pd.DataFrame(rows).to_csv(TABLES_DIR / f"{run_group}_metric_summary_last{LAST_WINDOW}_and_delta.csv", index=False)
    core_rows = [r for r in rows if r["metric"] in CORE_KPIS]
    pd.DataFrame(core_rows).to_csv(TABLES_DIR / f"{run_group}_core_kpi_summary.csv", index=False)


