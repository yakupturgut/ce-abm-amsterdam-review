def plot_metric_individual(main: Dict[str, Dict[str, object]], metric: str, y_label: str, title: str, out_dir: Path) -> None:
    data = {}
    for scenario in _ordered_scenarios(main.keys()):
        payload = main[scenario]
        cycles, arr = _stack_metric(payload, metric)
        if np.all(~np.isfinite(arr)):
            continue
        data[scenario] = (cycles, arr)
    if not data:
        return

    fig, ax = plt.subplots(figsize=FIGSIZE_TS)
    handles, labels = [], []
    for scenario in _ordered_scenarios(data.keys()):
        cycles, arr = data[scenario]
        mean, lower, upper = _mean_ci(arr)
        color = SCENARIO_COLORS.get(scenario, "black")
        line, = ax.plot(cycles, mean, color=color, label=SCENARIO_LABELS.get(scenario, scenario))
        ax.fill_between(cycles, lower, upper, color=color, alpha=0.18, linewidth=0)
        handles.append(line)
        labels.append(SCENARIO_LABELS.get(scenario, scenario))
    _add_title(ax, title)
    ax.set_xlabel("Month")
    ax.set_ylabel(y_label)
    _format_axes(ax)
    _legend_below(fig, handles, labels, ncol=2, bottom=0.30)
    _savefig(fig, out_dir / f"{metric}_mean_CI")
    plt.close(fig)

    vals, labels_box, colors = [], [], []
    for scenario in _ordered_scenarios(data.keys()):
        _, arr = data[scenario]
        v = _last_values(arr)
        if v.size:
            vals.append(v)
            labels_box.append(SCENARIO_LABELS_SHORT.get(scenario, scenario))
            colors.append(SCENARIO_COLORS.get(scenario, "0.5"))
    if vals:
        fig, ax = plt.subplots(figsize=FIGSIZE_BOX)
        bp = ax.boxplot(vals, patch_artist=True, showmeans=True,
                        medianprops={"color": "black", "linewidth": 1.2},
                        meanprops={"marker": "o", "markerfacecolor": "white", "markeredgecolor": "black", "markersize": 4})
        _apply_boxplot_style(bp, colors, alpha=0.55, lw=0.9)
        _style_categorical_axis(ax, labels_box, fontsize=7.0)
        _add_title(ax, f"{title}: last {LAST_WINDOW} months")
        ax.set_ylabel(y_label)
        _format_axes(ax)
        fig.tight_layout(pad=0.8)
        _savefig(fig, out_dir / f"{metric}_box_last{LAST_WINDOW}")
        plt.close(fig)

    vals, labels_box, colors = [], [], []
    for scenario in _ordered_scenarios(data.keys()):
        _, arr = data[scenario]
        v = _delta_values(arr)
        if v.size:
            vals.append(v)
            labels_box.append(SCENARIO_LABELS_SHORT.get(scenario, scenario))
            colors.append(SCENARIO_COLORS.get(scenario, "0.5"))
    if vals:
        fig, ax = plt.subplots(figsize=FIGSIZE_BOX)
        bp = ax.boxplot(vals, patch_artist=True, showmeans=True,
                        medianprops={"color": "black", "linewidth": 1.2},
                        meanprops={"marker": "o", "markerfacecolor": "white", "markeredgecolor": "black", "markersize": 4})
        _apply_boxplot_style(bp, colors, alpha=0.55, lw=0.9)
        _style_categorical_axis(ax, labels_box, fontsize=7.0)
        _add_title(ax, f"{title}: change")
        ax.set_ylabel(r"Change ($M_T-M_0$)")
        _format_axes(ax)
        fig.tight_layout(pad=0.8)
        _savefig(fig, out_dir / f"{metric}_box_delta")
        plt.close(fig)


