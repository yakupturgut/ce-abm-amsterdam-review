def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--run-group", default="latest", help="Run group ID, e.g. r20260604_163943, or 'latest'.")
    args = parser.parse_args()

    run_group = _find_latest_run_group() if args.run_group == "latest" else args.run_group
    manifest_csv = _manifest_path(run_group)
    if not manifest_csv.exists():
        raise FileNotFoundError(f"Manifest not found: {manifest_csv}")

    out_dir = FIGURES_ROOT / run_group
    out_main = out_dir / "main"
    out_beta = out_dir / "beta"
    out_obs = out_dir / "obs"
    out_main.mkdir(parents=True, exist_ok=True)

    print("============================================================")
    print("Building replicated figures")
    print("============================================================")
    print(f"Run group : {run_group}")
    print(f"Manifest  : {manifest_csv}")
    print(f"Figures   : {out_dir}")
    print("============================================================")

    manifest = pd.read_csv(manifest_csv)

    t0 = time.perf_counter()
    main_payload = _load_experiment(manifest, "main")

    if main_payload:
        write_summary_tables(main_payload, run_group)
        if BUILD_INDIVIDUAL_FIGURES:
            for metric, ylabel, title in METRICS:
                plot_metric_individual(main_payload, metric, ylabel, title, out_main)
        if BUILD_COMBINED_TRIPTYCH:
            for metric in COMBINED_METRICS:
                plot_triptych(main_payload, metric, out_main)
        if BUILD_DECOMPOSITION_FIGURES:
            plot_flow_decomposition(main_payload, out_main)
        if BUILD_K1_SPATIAL_HEATMAPS:
            plot_k1_spatial_heatmaps(main_payload, out_main, run_group)

    if BUILD_SENSITIVITY_FIGURES:
        beta_payload = _load_sensitivity(manifest, "beta", "beta_cei")
        plot_param_sensitivity(
            beta_payload,
            BETA_METRICS,
            param_name="beta_cei",
            label_func=lambda v: rf"$\beta_{{CEI}}={v:g}$",
            out_dir=out_beta,
            prefix="beta",
        )

        obs_payload = _load_sensitivity(manifest, "obs", "observability_prob")
        plot_param_sensitivity(
            obs_payload,
            OBS_METRICS,
            param_name="observability_prob",
            label_func=lambda v: rf"$p_{{obs}}={v:g}$",
            out_dir=out_obs,
            prefix="obs",
        )

    duration = time.perf_counter() - t0
    meta = {
        "run_group": run_group,
        "manifest": str(manifest_csv),
        "figures_dir": str(out_dir),
        "last_window": LAST_WINDOW,
        "delta_convention": "final_minus_initial",
        "excluded_main_metrics": ["D_labor_income", "MEAN_LABOR_INCOME", "P_voice_corr"],
        "k1_spatial_heatmaps": bool(BUILD_K1_SPATIAL_HEATMAPS),
        "duration_sec": duration,
    }
    (TABLES_DIR / f"{run_group}_figure_build_meta.json").write_text(json.dumps(meta, indent=2), encoding="utf-8")

    print("\n[DONE] Figure build complete.")
    print(f"[DONE] Figures saved under: {out_dir}")
    print(f"[DONE] Summary tables saved under: {TABLES_DIR}")


if __name__ == "__main__":
    main()
