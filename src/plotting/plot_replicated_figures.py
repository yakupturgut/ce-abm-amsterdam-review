def plot_param_sensitivity(
    sens: Dict[float, Dict[str, Dict[str, object]]],
    metrics: List[Tuple[str, str, str]],
    param_name: str,
    label_func,
    out_dir: Path,
    prefix: str,
) -> None:
    if not sens:
        return
    out_dir.mkdir(parents=True, exist_ok=True)

    rows = []
    for param_value, scenarios in sens.items():
        for scenario, payload in scenarios.items():
            for metric, _, _ in metrics:
                _, arr = _stack_metric(payload, metric)
                if np.all(~np.isfinite(arr)):
                    continue
                last = _summ(_last_values(arr))
                delta = _summ(_delta_values(arr))
                rows.append({
                    param_name: param_value,
                    "scenario": scenario,
                    "scenario_label": SCENARIO_LABELS.get(scenario, scenario),
                    "metric": metric,
                    "last24_mean": last["mean"],
                    "last24_ci95_low": last["ci_low"],
                    "last24_ci95_high": last["ci_high"],
                    "delta_mean": delta["mean"],
                    "delta_ci95_low": delta["ci_low"],
                    "delta_ci95_high": delta["ci_high"],
                })
    pd.DataFrame(rows).to_csv(TABLES_DIR / f"{out_dir.parent.name}_{prefix}_summary.csv", index=False)

    scenarios = _ordered_scenarios({s for _, payload in sens.items() for s in payload.keys()})
    for scenario in scenarios:
        for metric, ylabel, title in metrics:
            fig, ax = plt.subplots(figsize=FIGSIZE_SENS)
            handles, labels = [], []
            any_plotted = False
            for param_value in sorted(sens.keys(), reverse=True):
                if scenario not in sens[param_value]:
                    continue
                payload = sens[param_value][scenario]
                cycles, arr = _stack_metric(payload, metric)
                if np.all(~np.isfinite(arr)):
                    continue
                mean, lower, upper = _mean_ci(arr)
                line, = ax.plot(cycles, mean, label=label_func(param_value))
                ax.fill_between(cycles, lower, upper, alpha=0.13, linewidth=0)
                handles.append(line)
                labels.append(label_func(param_value))
                any_plotted = True
            if not any_plotted:
                plt.close(fig)
                continue
            _add_title(ax, f"{title}\n{SCENARIO_LABELS.get(scenario, scenario)}")
            ax.set_xlabel("Month")
            ax.set_ylabel(ylabel)
            _format_axes(ax)
            _legend_below(fig, handles, labels, ncol=min(3, len(labels)), bottom=0.29)
            short_metric = metric.replace("UNEMPLOYMENT", "UNEMP").replace("MEAN_LOCAL_CE_ACTIVATION", "K1LOCAL").replace("CE_INTENSITY", "CEINT")
            _savefig(fig, out_dir / f"{short_metric}_{prefix}_{scenario}")
            plt.close(fig)


