def _resolve_k1_npz(row: Dict[str, Any]) -> Optional[Path]:
    val = str(row.get("k1_spatial_npz", "") or "")
    if val and val.lower() != "nan":
        p = Path(val)
        if p.exists():
            return p
    run_dir = Path(str(row.get("run_dir", "") or ""))
    p = run_dir / "K1_spatial_ce_intensity.npz"
    if p.exists():
        return p
    return None


def _load_k1_spatial(main: Dict[str, Dict[str, object]]) -> Dict[str, Dict[str, Any]]:
    result: Dict[str, Dict[str, Any]] = {}
    for scenario, payload in main.items():
        meta_rows: List[Dict[str, Any]] = payload.get("meta", [])  # type: ignore
        last_mean, last_temporal_sd, change, final = [], [], [], []
        infra = None
        files = []
        for row in meta_rows:
            p = _resolve_k1_npz(row)
            if p is None:
                continue
            try:
                z = np.load(p, allow_pickle=True)
                last_mean.append(np.asarray(z["last_window_mean_grid"], dtype=float))
                last_temporal_sd.append(np.asarray(z["last_window_temporal_sd_grid"], dtype=float))
                change.append(np.asarray(z["change_grid"], dtype=float))
                final.append(np.asarray(z["final_grid"], dtype=float))
                if infra is None:
                    infra = {
                        "ptype": np.asarray(z.get("ptype", np.empty((0, 0))), dtype=int),
                        "hub_cells": np.asarray(z.get("hub_cells", np.empty((0, 2))), dtype=int),
                        "waste_cells": np.asarray(z.get("waste_cells", np.empty((0, 2))), dtype=int),
                        "container_cells": np.asarray(z.get("container_cells", np.empty((0, 2))), dtype=int),
                        "recycling_cells": np.asarray(z.get("recycling_cells", np.empty((0, 2))), dtype=int),
                    }
                files.append(str(p))
            except Exception as exc:
                print(f"[WARN] Could not read K1 spatial file {p}: {exc}")
        if last_mean:
            last_mean_stack = np.stack(last_mean, axis=0)
            last_temporal_stack = np.stack(last_temporal_sd, axis=0)
            change_stack = np.stack(change, axis=0)
            final_stack = np.stack(final, axis=0)
            result[scenario] = {
                "last_mean_stack": last_mean_stack,
                "last_temporal_sd_stack": last_temporal_stack,
                "change_stack": change_stack,
                "final_stack": final_stack,
                "last_mean_across_reps": np.nanmean(last_mean_stack, axis=0),
                "last_crossrep_sd": np.nanstd(last_mean_stack, axis=0, ddof=1) if last_mean_stack.shape[0] > 1 else np.zeros_like(last_mean_stack[0]),
                "last_temporal_sd_mean": np.nanmean(last_temporal_stack, axis=0),
                "change_mean": np.nanmean(change_stack, axis=0),
                "final_mean": np.nanmean(final_stack, axis=0),
                "infra": infra,
                "files": files,
                "n_runs": len(files),
            }
    return result


def _grid_limits(grids: List[np.ndarray], *, force_zero: bool = True, upper_percentile: float = 99.0) -> Tuple[float, float]:
    vals = np.concatenate([g[np.isfinite(g)].ravel() for g in grids if np.isfinite(g).any()]) if grids else np.array([])
    if vals.size == 0:
        return 0.0, 1.0
    vmin = 0.0 if force_zero else float(np.nanpercentile(vals, 1.0))
    vmax = float(np.nanpercentile(vals, upper_percentile))
    if not np.isfinite(vmax) or abs(vmax - vmin) < 1e-12:
        vmax = vmin + 1e-6
    return vmin, vmax


def _overlay_infrastructure(ax: plt.Axes, infra: Optional[Dict[str, np.ndarray]]) -> None:
    if not infra:
        return
    for key, marker, size in [
        ("container_cells", "^", 10),
        ("recycling_cells", "D", 9),
        ("hub_cells", "*", 18),
        ("waste_cells", "x", 12),
    ]:
        pts = np.asarray(infra.get(key, np.empty((0, 2))))
        if pts.size == 0:
            continue
        pts = pts.reshape((-1, 2))
        ax.scatter(pts[:, 0], pts[:, 1], marker=marker, s=size, c="black", linewidths=0.45, alpha=0.70, zorder=3)


def _make_k1_spatial_grid_figure(
    spatial: Dict[str, Dict[str, Any]],
    key: str,
    title: str,
    cbar_label: str,
    *,
    cmap: str = "viridis",
) -> Optional[plt.Figure]:
    """Create one four-scenario K1 spatial heat-map figure with a safe layout.

    The earlier implementation used ``fig.colorbar(..., ax=axes)`` and a very
    tight ``suptitle`` area. In LaTeX/PDF viewers this caused the global title,
    panel titles and colorbar to overlap. This version uses GridSpec with a
    dedicated colorbar column and more vertical headroom.
    """
    scenarios = _ordered_scenarios(spatial.keys())
    if not scenarios:
        return None

    grids = [np.asarray(spatial[s][key], dtype=float) for s in scenarios]
    vmin, vmax = _grid_limits(grids, force_zero=True, upper_percentile=99.0)

    n = len(scenarios)
    ncols = min(4, n)
    nrows = int(math.ceil(n / ncols))

    # Wider and taller than the previous version; the extra width is reserved
    # for a dedicated colorbar axis, not taken from the map panels.
    fig_w = 2.85 * ncols + 0.95
    fig_h = 3.45 * nrows + 0.75
    fig = plt.figure(figsize=(fig_w, fig_h))

    gs = fig.add_gridspec(
        nrows=nrows,
        ncols=ncols + 1,
        width_ratios=[1.0] * ncols + [0.060],
        wspace=0.08,
        hspace=0.30,
    )

    axes: List[plt.Axes] = []
    im = None
    for idx, scenario in enumerate(scenarios):
        r = idx // ncols
        c = idx % ncols
        ax = fig.add_subplot(gs[r, c])
        axes.append(ax)

        grid = np.asarray(spatial[scenario][key], dtype=float)
        im = ax.imshow(
            grid.T,
            origin="lower",
            cmap=cmap,
            vmin=vmin,
            vmax=vmax,
            interpolation="nearest",
        )
        _overlay_infrastructure(ax, spatial[scenario].get("infra"))
        ax.set_title(
            f"{SCENARIO_LABELS.get(scenario, scenario)}\n(n={spatial[scenario].get('n_runs', 0)})",
            fontsize=8.2,
            pad=7.0,
        )
        ax.set_xticks([])
        ax.set_yticks([])
        ax.grid(False)
        for spine in ax.spines.values():
            spine.set_linewidth(0.6)

    # Keep empty cells blank when fewer than four scenarios are present.
    for idx in range(n, nrows * ncols):
        ax = fig.add_subplot(gs[idx // ncols, idx % ncols])
        ax.axis("off")

    if im is not None:
        cax = fig.add_subplot(gs[:, -1])
        cbar = fig.colorbar(im, cax=cax)
        cbar.set_label(cbar_label, fontsize=9.2, labelpad=10)
        cbar.ax.tick_params(labelsize=8.2, width=0.7, length=3)

    if SHOW_TITLES:
        fig.suptitle(title, y=0.985, fontsize=10.6, fontweight="bold")

    # One-row spatial figures need substantial space above the map panels for
    # both the global title and the per-scenario titles. This avoids the overlap
    # visible in the previous K1 spatial PDFs.
    if nrows == 1:
        fig.subplots_adjust(left=0.025, right=0.935, top=0.925, bottom=0.070)
    else:
        fig.subplots_adjust(left=0.035, right=0.935, top=0.865, bottom=0.060)

    return fig


def _plot_k1_spatial_grid(
    spatial: Dict[str, Dict[str, Any]],
    key: str,
    title: str,
    cbar_label: str,
    out_base: Path,
    *,
    cmap: str = "viridis",
) -> None:
    fig = _make_k1_spatial_grid_figure(
        spatial,
        key=key,
        title=title,
        cbar_label=cbar_label,
        cmap=cmap,
    )
    if fig is None:
        return
    _savefig(fig, out_base)
    plt.close(fig)


def _save_k1_spatial_pdf_bundle(spatial: Dict[str, Dict[str, Any]], spatial_dir: Path) -> Path:
    """Save the four K1 spatial diagnostics in one multi-page PDF.

    This is useful for Overleaf and review because opening four separate PDFs
    can feel like an animation. The individual PDFs are still saved for direct
    figure inclusion, while this bundle is convenient as an appendix/supplement.
    """
    specs = [
        (
            "last_mean_across_reps",
            f"K1 spatial distribution: last {LAST_WINDOW}-month mean local CE activation",
            "Mean local CE activation",
            "viridis",
        ),
        (
            "last_crossrep_sd",
            f"K1 spatial uncertainty: cross-replication SD of last {LAST_WINDOW}-month mean",
            "Cross-replication SD",
            "magma",
        ),
        (
            "last_temporal_sd_mean",
            f"K1 spatial variability: within-run temporal SD over the last {LAST_WINDOW} months",
            "Temporal SD",
            "magma",
        ),
        (
            "change_mean",
            "K1 spatial change: final minus initial local CE activation",
            "Final-minus-initial change",
            "viridis",
        ),
    ]

    pdf_path = spatial_dir / "K1_spatial_heatmaps_4page.pdf"
    with PdfPages(pdf_path) as pdf:
        for key, title, cbar_label, cmap in specs:
            fig = _make_k1_spatial_grid_figure(
                spatial,
                key=key,
                title=title,
                cbar_label=cbar_label,
                cmap=cmap,
            )
            if fig is None:
                continue
            pdf.savefig(fig, bbox_inches="tight")
            plt.close(fig)
    return pdf_path


def plot_k1_spatial_heatmaps(main: Dict[str, Dict[str, object]], out_dir: Path, run_group: str) -> None:
    spatial = _load_k1_spatial(main)
