def _find_latest_run_group() -> str:
    manifests = sorted(TABLES_DIR.glob("r*_run_manifest.csv"), key=lambda p: p.stat().st_mtime, reverse=True)
    if not manifests:
        raise FileNotFoundError(f"No run manifest found under {TABLES_DIR}")
    return manifests[0].name.replace("_run_manifest.csv", "")


def _manifest_path(run_group: str) -> Path:
    return TABLES_DIR / f"{run_group}_run_manifest.csv"


def _savefig(fig: plt.Figure, out_base: Path) -> None:
    out_base.parent.mkdir(parents=True, exist_ok=True)
    for fmt in SAVE_FORMATS:
        path = out_base.with_suffix(f".{fmt}")
        if fmt.lower() == "png":
            fig.savefig(path, format=fmt, dpi=600, bbox_inches="tight")
        else:
            fig.savefig(path, format=fmt, bbox_inches="tight")


def _read_log(csv_path: Path) -> pd.DataFrame:
    df = pd.read_csv(csv_path)
    if "cycle" not in df.columns:
        for cand in ["Cycle", "CYCLE", "t", "month"]:
            if cand in df.columns:
                df = df.rename(columns={cand: "cycle"})
                break
    return df


def _load_experiment(manifest: pd.DataFrame, experiment: str) -> Dict[str, Dict[str, object]]:
    sub = manifest[(manifest["experiment"] == experiment) & (manifest["status"] == "ok")].copy()
    result: Dict[str, Dict[str, object]] = {}
    for scenario, sgrp in sub.groupby("scenario"):
        reps = []
        cycles_ref = None
        meta_rows = []
        for _, row in sgrp.sort_values("replicate").iterrows():
            csv_path = Path(str(row.get("master_csv", "")))
            if not csv_path.exists():
                continue
            df = _read_log(csv_path)
            if "cycle" not in df.columns:
                continue
            cycles = df["cycle"].to_numpy(dtype=float)
            if cycles_ref is None:
                cycles_ref = cycles
            reps.append(df)
            meta_rows.append(row.to_dict())
        if reps and cycles_ref is not None:
            result[scenario] = {"cycles": cycles_ref, "replicates": reps, "meta": meta_rows}
    return result


def _load_sensitivity(manifest: pd.DataFrame, experiment: str, param_col: str) -> Dict[float, Dict[str, Dict[str, object]]]:
    sub = manifest[(manifest["experiment"] == experiment) & (manifest["status"] == "ok")].copy()
    if param_col not in sub.columns:
        return {}
    sub[param_col] = pd.to_numeric(sub[param_col], errors="coerce")
    result: Dict[float, Dict[str, Dict[str, object]]] = {}
    for param_value, pgrp in sub.groupby(param_col):
        scen_payload: Dict[str, Dict[str, object]] = {}
        for scenario, sgrp in pgrp.groupby("scenario"):
            reps = []
            cycles_ref = None
            for _, row in sgrp.sort_values("replicate").iterrows():
                csv_path = Path(str(row.get("master_csv", "")))
                if not csv_path.exists():
                    continue
                df = _read_log(csv_path)
                if "cycle" not in df.columns:
                    continue
                cycles = df["cycle"].to_numpy(dtype=float)
                if cycles_ref is None:
                    cycles_ref = cycles
                reps.append(df)
            if reps and cycles_ref is not None:
                scen_payload[scenario] = {"cycles": cycles_ref, "replicates": reps, param_col: float(param_value)}
        if scen_payload:
            result[float(param_value)] = scen_payload
    return result


def _column_for_metric(df: pd.DataFrame, metric: str) -> Optional[str]:
    for cand in METRIC_ALIASES.get(metric, [metric]):
        if cand in df.columns:
            return cand
    return None


def _stack_metric(payload: Dict[str, object], metric: str) -> Tuple[np.ndarray, np.ndarray]:
    cycles = np.asarray(payload["cycles"], dtype=float)
    reps: List[pd.DataFrame] = payload["replicates"]  # type: ignore
    series = []
    for df in reps:
        col = _column_for_metric(df, metric)
        if col is None:
            series.append(np.full(len(cycles), np.nan, dtype=float))
        else:
            arr = df[col].to_numpy(dtype=float)
            out = np.full(len(cycles), np.nan, dtype=float)
            m = min(len(cycles), len(arr))
            out[:m] = arr[:m]
            series.append(out)
    return cycles, np.vstack(series)


def _mean_ci(arr: np.ndarray) -> Tuple[np.ndarray, np.ndarray, np.ndarray]:
    mean = np.nanmean(arr, axis=0)
    n_eff = np.sum(np.isfinite(arr), axis=0)
    std = np.nanstd(arr, axis=0, ddof=1)
    hw = np.zeros_like(mean, dtype=float)
    valid = n_eff > 1
    hw[valid] = 1.96 * std[valid] / np.sqrt(n_eff[valid])
    return mean, mean - hw, mean + hw


def _last_values(arr: np.ndarray, window: int = LAST_WINDOW) -> np.ndarray:
    w = min(window, arr.shape[1])
    vals = np.nanmean(arr[:, -w:], axis=1)
    return vals[np.isfinite(vals)]


def _delta_values(arr: np.ndarray) -> np.ndarray:
    vals = arr[:, -1] - arr[:, 0]
    return vals[np.isfinite(vals)]


def _summ(vals: np.ndarray) -> Dict[str, float]:
    vals = vals[np.isfinite(vals)]
    n = vals.size
    if n == 0:
        return {"n": 0, "mean": np.nan, "sd": np.nan, "ci_low": np.nan, "ci_high": np.nan, "median": np.nan, "min": np.nan, "max": np.nan}
    mean = float(np.mean(vals))
    sd = float(np.std(vals, ddof=1)) if n > 1 else 0.0
    hw = 1.96 * sd / math.sqrt(n) if n > 1 else 0.0
    return {"n": int(n), "mean": mean, "sd": sd, "ci_low": mean - hw, "ci_high": mean + hw, "median": float(np.median(vals)), "min": float(np.min(vals)), "max": float(np.max(vals))}


def _metric_lookup(metric: str) -> Tuple[str, str]:
    for m, y, title in METRICS:
        if m == metric:
            return y, title
    return metric, metric


def _format_axes(ax: plt.Axes) -> None:
    ax.tick_params(axis="both", which="major", length=3.0, width=0.8)
    for spine in ax.spines.values():
        spine.set_linewidth(0.8)


def _style_categorical_axis(ax: plt.Axes, labels: List[str], *, rotation: float = 0.0, fontsize: float = 7.0) -> None:
    positions = np.arange(1, len(labels) + 1)
    ax.set_xticks(positions)
    ax.set_xticklabels(labels, rotation=rotation, ha="center", linespacing=0.9)
    ax.tick_params(axis="x", labelsize=fontsize, pad=2)
    ax.margins(x=0.06)


def _apply_boxplot_style(bp: Dict[str, Any], colors: List[str], *, alpha: float = 0.55, lw: float = 0.9) -> None:
    for box, color in zip(bp["boxes"], colors):
        box.set_facecolor(color)
        box.set_alpha(alpha)
        box.set_linewidth(lw)
    for whisker in bp.get("whiskers", []):
        whisker.set_linewidth(0.85)
    for cap in bp.get("caps", []):
        cap.set_linewidth(0.85)


def _add_title(ax: plt.Axes, title: str) -> None:
    if SHOW_TITLES:
        ax.set_title(title, pad=6)


def _legend_below(fig: plt.Figure, handles, labels, ncol: int = 2, bottom: float = 0.28) -> None:
    fig.legend(handles, labels, loc="lower center", bbox_to_anchor=(0.5, 0.01), ncol=ncol, frameon=True)
    fig.subplots_adjust(bottom=bottom)


